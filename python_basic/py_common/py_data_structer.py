# -*- coding: utf-8 -*-
def data_kind():
    # num, 数字
    myNum = 123
    print(myNum)
    # string, 字符串
    myStr = 'hello world!'
    print(myStr)
    # list, 列表
    myList = [1, 2, 34, 5, "hello"]
    print(myList)
    # tuple, 元组
    myTuple = (1, 2, 3, 4, "jjjjasd", 'mg', [1, 2, 3])
    print(myTuple)
    # set, 集合
    mySet = {1, 2, 3, 4, 5}
    print(mySet)
    # dictionary, 字典
    myDict = {"hello": 999, "myhobby": "sleep", "dream": [1, 23, 3, 'Hello']}
    print(myDict)


if __name__ == "__main__":
    data_kind()
